#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*temp;

	temp = *lst;
	if (!lst)
		return ;
	while (temp->next)
	{
		temp = temp->next;
	}
	temp->next = new;
	new->next = NULL;
}
